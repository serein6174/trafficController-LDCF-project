module arrow_display #(
    parameter START_X = 300,  // 字符左上角X坐标
    parameter START_Y = 200   // 字符左上角Y坐标
) (
    input [9:0] vga_col,      
    input [8:0] vga_row,     
    input mod,                
    output reg [11:0] pixel_data 
);
    localparam CHAR_WIDTH = 32;
    localparam CHAR_HEIGHT = 32;
    wire in_char_area = (vga_col >= START_X) && 
                       (vga_col < START_X + CHAR_WIDTH) &&
                       (vga_row >= START_Y) && 
                       (vga_row < START_Y + CHAR_HEIGHT);
    wire [4:0] char_col = vga_col - START_X; 
    wire [4:0] char_row = vga_row - START_Y; 
    reg [31:0] char_line; // 每行32位像素数据
    
    always @(*) begin
        if (in_char_area) begin
            if (mod == 0) begin 
                case (char_row)
                    0: char_line = 32'b00000000000000000000000000000000;
                    1: char_line = 32'b00000000000000000000000000000000;
                    2: char_line = 32'b00000000000000000000000000000000;
                    3: char_line = 32'b00000000011110000000000000000000;
                    4: char_line = 32'b00000001111000000000000000000000;
                    5: char_line = 32'b00000111100000000000000000000000;
                    6: char_line = 32'b00011110000000000000000000000000;
                    7: char_line = 32'b11111111111111111111111111111111;
                    8: char_line = 32'b11111111111111111111111111111111;
                    9: char_line = 32'b11111111111111111111111111111111;
                    10: char_line = 32'b11111111111111111111111111111111;
                    11: char_line = 32'b00011110000000000000000000000000;
                    12: char_line = 32'b00000111100000000000000000000000;
                    13: char_line = 32'b00000001111000000000000000000000;
                    14: char_line = 32'b00000000011110000000000000000000;
                    15: char_line = 32'b00000000000000000000000000000000;
                    16: char_line = 32'b00000000000000000000000000000000;
                    17: char_line = 32'b00000000000000000000000000000000;
                    18: char_line = 32'b00000000000000000000000000000000;
                    19: char_line = 32'b00000000000000000000111100000000;
                    20: char_line = 32'b00000000000000000000001111000000;
                    21: char_line = 32'b00000000000000000000000011110000;
                    22: char_line = 32'b00000000000000000000000000111100;
                    23: char_line = 32'b11111111111111111111111111111111;
                    24: char_line = 32'b11111111111111111111111111111111;
                    25: char_line = 32'b11111111111111111111111111111111;
                    26: char_line = 32'b11111111111111111111111111111111;
                    27: char_line = 32'b00000000000000000000000000111100;
                    28: char_line = 32'b00000000000000000000000011110000;
                    29: char_line = 32'b00000000000000000000001111000000;
                    30: char_line = 32'b00000000000000000000111100000000;
                    31: char_line = 32'b00000000000000000000000000000000;
                    default: char_line = 32'b0;
                endcase
            end else begin
                case (char_row)
                   0: char_line = 32'b00000111100000000000011110000000;
                    1: char_line = 32'b00000111100000000000011110000000;
                    2: char_line = 32'b00000111100000000000011110000000;
                    3: char_line = 32'b00000111100000000000111111000000;
                    4: char_line = 32'b00000111100000000000111111000000;
                    5: char_line = 32'b00000111100000000001111111100000;
                    6: char_line = 32'b00000111100000000001111111100000;
                    7: char_line = 32'b00000111100000000011011110110000;
                    8: char_line = 32'b00000111100000000011011110110000;
                    9: char_line = 32'b00000111100000000110011110011000;
                    10: char_line = 32'b00000111100000000110011110011000;
                    11: char_line = 32'b00000111100000000100011110001000;
                    12: char_line = 32'b00000111100000000100011110001000;
                    13: char_line = 32'b00000111100000000000011110000000;
                    14: char_line = 32'b00000111100000000000011110000000;
                    15: char_line = 32'b00000111100000000000011110000000;
                    16: char_line = 32'b00000111100000000000011110000000;
                    17: char_line = 32'b00000111100000000000011110000000;
                    18: char_line = 32'b00000111100000000000011110000000;
                    19: char_line = 32'b00000111100000000000011110000000;
                    20: char_line = 32'b01000111100010000000011110000000;
                    21: char_line = 32'b01000111100010000000011110000000;
                    22: char_line = 32'b01100111100110000000011110000000;
                    23: char_line = 32'b01100111100110000000011110000000;
                    24: char_line = 32'b00110111101100000000011110000000;
                    25: char_line = 32'b00110111101100000000011110000000;
                    26: char_line = 32'b00011111111000000000011110000000;
                    27: char_line = 32'b00011111111000000000011110000000;
                    28: char_line = 32'b00001111110000000000011110000000;
                    29: char_line = 32'b00001111110000000000011110000000;
                    30: char_line = 32'b00000111100000000000011110000000;
                    31: char_line = 32'b00000111100000000000011110000000;
                    default: char_line = 32'b0;
                endcase
            end
        end else begin
            char_line = 32'b0;
        end
    end

    // 提取当前像素点
    wire pixel_on = char_line[31 - char_col]; // 高位对应左侧像素
    
    // 输出颜色 (绿色)
    always @(*) begin
        if (pixel_on) begin
            pixel_data = 12'h0F0; // 绿色 (bbbb_0000_1111_0000)
        end else begin
            pixel_data = 12'h000; // 黑色背景
        end
    end

endmodule